class Depto:
    __numero: int
    __nombre: str
def __init__(self, num, nom):
    self.__numero = num
    self.__nombre = nom

def getnumero(self):
    return self.__numero

def setnumero(self, otronum):
    self.__numero= otronum

def getnombre(self):
    return self.__nombre
def setnombre(self, otronom):
    self.__nombre= otronom